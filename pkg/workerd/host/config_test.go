func mod(name string, t computev1alpha1.ModuleType, path string) computev1alpha1.Module {
	return computev1alpha1.Module{Name: name, Type: t, Path: path}
}

// TestBuildWorkerdConfig_Golden locks the exact textual capnp shape for a
// canonical backend-mode worker. Any change to the emitted format is a
// deliberate edit to this golden.
func TestBuildWorkerdConfig_Golden(t *testing.T) {
	got, err := BuildWorkerdConfig(BuildInput{
		Manifest: computev1alpha1.BundleManifest{
			Modules:            []computev1alpha1.Module{mod("worker.js", computev1alpha1.ESModule, "worker.js")},
			CompatibilityDate:  "2024-01-01",
			CompatibilityFlags: []string{"nodejs_compat"},
		},
		Config: computev1alpha1.ServiceConfigSpec{
			Env: []computev1alpha1.EnvVar{{Name: "API_URL", Value: "https://example.com"}},
		},
		Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
	})
	if err != nil {
		t.Fatalf("BuildWorkerdConfig: %v", err)
	}
	want := `# Generated by apoxy workerd-host. DO NOT EDIT.
using Workerd = import "/workerd/workerd.capnp";

const config :Workerd.Config = (
  services = [
    (name = "main", worker = .mainWorker),
  ],
  sockets = [
    (name = "http", address = "*:8080", http = (), service = "main"),
  ],
);

const mainWorker :Workerd.Worker = (
  compatibilityDate = "2024-01-01",
  compatibilityFlags = ["nodejs_compat"],
  modules = [
    (name = "worker.js", esModule = embed "worker.js"),
  ],
  bindings = [
    (name = "API_URL", text = "https://example.com"),
  ],
);
`
	if got != want {
		t.Errorf("config mismatch:\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

func TestBuildWorkerdConfig(t *testing.T) {
	esOnly := []computev1alpha1.Module{mod("index.js", computev1alpha1.ESModule, "index.js")}

	cases := []struct {
		name      string
		in        BuildInput
		wantErr   error    // exact sentinel, if expected
		wantSubs  []string // substrings that must be present on success
		absentSub []string // substrings that must NOT be present
	}{
		{
			name: "filter socket unsupported",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: esOnly, CompatibilityDate: "2024-01-01"},
				Socket:   SocketSpec{Kind: FilterSocket, Addr: "*:8080"},
			},
			wantErr: errFilterSocketUnsupported,
		},
		{
			name: "missing compatibility date",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: esOnly},
				Socket:   SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			},
			wantErr: errNoCompatibilityDate,
		},
		{
			name: "no esModule entrypoint",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{
					Modules:           []computev1alpha1.Module{mod("data.bin", computev1alpha1.DataModule, "data.bin")},
					CompatibilityDate: "2024-01-01",
				},
				Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			},
			wantErr: errNoEntrypoint,
		},
		{
			name: "runtime compatibilityDate overrides manifest",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: esOnly, CompatibilityDate: "2024-01-01"},
				Config: computev1alpha1.ServiceConfigSpec{
					Runtime: &computev1alpha1.ServiceRuntime{CompatibilityDate: "2025-06-01"},
				},
				Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			},
			wantSubs:  []string{`compatibilityDate = "2025-06-01"`},
			absentSub: []string{`compatibilityDate = "2024-01-01"`},
		},
		{
			name: "compatibilityFlags union dedupes and preserves order",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{
					Modules:            esOnly,
					CompatibilityDate:  "2024-01-01",
					CompatibilityFlags: []string{"a", "b"},
				},
				Config: computev1alpha1.ServiceConfigSpec{
					Runtime: &computev1alpha1.ServiceRuntime{CompatibilityFlags: []string{"b", "c"}},
				},
				Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			},
			wantSubs: []string{`compatibilityFlags = ["a", "b", "c"]`},
		},
		{
			name: "assets disk service and binding emitted",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{
					Modules:           esOnly,
					CompatibilityDate: "2024-01-01",
					AssetsPrefix:      "/static",
				},
				Socket:    SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
				AssetsDir: "/assets",
			},
			wantSubs: []string{
				`(name = "assets", disk = (path = "/assets", writable = false)),`,
				`(name = "ASSETS", service = "assets"),`,
			},
		},
		{
			name: "no assets means no disk service",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: esOnly, CompatibilityDate: "2024-01-01"},
				Socket:   SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			},
			absentSub: []string{"disk = (", `service = "assets"`},
		},
		{
			name: "typed binding is unsupported in M1",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: esOnly, CompatibilityDate: "2024-01-01"},
				Config: computev1alpha1.ServiceConfigSpec{
					Bindings: []computev1alpha1.Binding{{Name: "DB", Type: computev1alpha1.KVBindingType}},
				},
				Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			},
			wantSubs: nil,
			wantErr:  errSentinelSubstr("not supported in M1"),
		},
		{
			name: "unix socket address passes through",
			in: BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: esOnly, CompatibilityDate: "2024-01-01"},
				Socket:   SocketSpec{Kind: HTTPSocket, Addr: "unix:/run/workerd/r1.sock"},
			},
			wantSubs: []string{`address = "unix:/run/workerd/r1.sock"`},
		},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got, err := BuildWorkerdConfig(tc.in)
			if tc.wantErr != nil {
				if err == nil {
					t.Fatalf("want error %v, got nil (output:\n%s)", tc.wantErr, got)
				}
				if sub, ok := tc.wantErr.(substrErr); ok {
					if !strings.Contains(err.Error(), string(sub)) {
						t.Fatalf("error %q does not contain %q", err.Error(), string(sub))
					}
					return
				}
				if err != tc.wantErr {
					t.Fatalf("want error %v, got %v", tc.wantErr, err)
				}
				return
			}
			if err != nil {
				t.Fatalf("unexpected error: %v", err)
			}
			for _, s := range tc.wantSubs {
				if !strings.Contains(got, s) {
					t.Errorf("output missing %q:\n%s", s, got)
				}
			}
			for _, s := range tc.absentSub {
				if strings.Contains(got, s) {
					t.Errorf("output unexpectedly contains %q:\n%s", s, got)
				}
			}
		})
	}
}

// TestBuildWorkerdConfig_EveryModuleType asserts each compute ModuleType maps to
// the matching workerd capnp union field.
func TestBuildWorkerdConfig_EveryModuleType(t *testing.T) {
	cases := []struct {
		typ   computev1alpha1.ModuleType
		field string
	}{
		{computev1alpha1.ESModule, "esModule"},
		{computev1alpha1.CommonJSModule, "commonJsModule"},
		{computev1alpha1.TextModule, "text"},
		{computev1alpha1.DataModule, "data"},
		{computev1alpha1.JSONModule, "json"},
		{computev1alpha1.WasmModule, "wasm"},
	}
	for _, tc := range cases {
		t.Run(string(tc.typ), func(t *testing.T) {
			// Always include an esModule entrypoint, plus the module under test.
			mods := []computev1alpha1.Module{mod("index.js", computev1alpha1.ESModule, "index.js")}
			if tc.typ != computev1alpha1.ESModule {
				mods = append(mods, mod("extra", tc.typ, "extra.bin"))
			}
			got, err := BuildWorkerdConfig(BuildInput{
				Manifest: computev1alpha1.BundleManifest{Modules: mods, CompatibilityDate: "2024-01-01"},
				Socket:   SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
			})
			if err != nil {
				t.Fatalf("BuildWorkerdConfig: %v", err)
			}
			want := tc.field + " = embed "
			if !strings.Contains(got, want) {
				t.Errorf("output missing %q:\n%s", want, got)
			}
		})
	}
}

// TestOrderModules_EntrypointFirst asserts the first esModule is moved to the
// front while the relative order of the rest is preserved.
func TestOrderModules_EntrypointFirst(t *testing.T) {
	got, err := BuildWorkerdConfig(BuildInput{
		Manifest: computev1alpha1.BundleManifest{
			Modules: []computev1alpha1.Module{
				mod("a.txt", computev1alpha1.TextModule, "a.txt"),
				mod("main.js", computev1alpha1.ESModule, "main.js"),
				mod("z.txt", computev1alpha1.TextModule, "z.txt"),
			},
			CompatibilityDate: "2024-01-01",
		},
		Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
	})
	if err != nil {
		t.Fatalf("BuildWorkerdConfig: %v", err)
	}
	iMain := strings.Index(got, `name = "main.js"`)
	iA := strings.Index(got, `name = "a.txt"`)
	iZ := strings.Index(got, `name = "z.txt"`)
	if iMain < 0 || iA < 0 || iZ < 0 {
		t.Fatalf("missing a module in output:\n%s", got)
	}
	if !(iMain < iA && iA < iZ) {
		t.Errorf("module order wrong: main=%d a=%d z=%d (want main<a<z)\n%s", iMain, iA, iZ, got)
	}
}

// TestBuildWorkerdConfig_Deterministic asserts identical input renders
// byte-identical output across runs.
func TestBuildWorkerdConfig_Deterministic(t *testing.T) {
	in := BuildInput{
		Manifest: computev1alpha1.BundleManifest{
			Modules:            []computev1alpha1.Module{mod("index.js", computev1alpha1.ESModule, "index.js")},
			CompatibilityDate:  "2024-01-01",
			CompatibilityFlags: []string{"a", "b"},
		},
		Config: computev1alpha1.ServiceConfigSpec{
			Env: []computev1alpha1.EnvVar{{Name: "X", Value: "1"}, {Name: "Y", Value: "2"}},
		},
		Socket: SocketSpec{Kind: HTTPSocket, Addr: "*:8080"},
	}
	first, err := BuildWorkerdConfig(in)
	if err != nil {
		t.Fatalf("BuildWorkerdConfig: %v", err)
	}
	for i := 0; i < 5; i++ {
		again, err := BuildWorkerdConfig(in)
		if err != nil {
			t.Fatalf("BuildWorkerdConfig (run %d): %v", i, err)
		}
		if again != first {
			t.Fatalf("non-deterministic output on run %d", i)
		}
	}
}

func TestCapnpStr_Escaping(t *testing.T) {
	cases := map[string]string{
		`plain`:      `"plain"`,
		`a"b`:        `"a\"b"`,
		`a\b`:        `"a\\b"`,
		"tab\tafter": `"tab\tafter"`,
		"nl\nafter":  `"nl\nafter"`,
	}
	for in, want := range cases {
		if got := capnpStr(in); got != want {
			t.Errorf("capnpStr(%q) = %q, want %q", in, got, want)
		}
	}
}

// substrErr lets a table case assert an error contains a substring rather than
// matching a sentinel exactly.
type substrErr string

func (s substrErr) Error() string { return string(s) }

func errSentinelSubstr(s string) error { return substrErr(s) }
